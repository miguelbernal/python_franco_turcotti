# Imprimir los numeros impares del 1 al 50
i = 1
print("Número pares")
while i <= 50:
    if i % 2 == 0:
        print(i)
    i += 1

i = 1
print("Número impares")
while i <= 50:
    if i % 2 != 0:
        print(i)
    i += 1

i = 2
print("Número pares incrementando 2 a la variable i")
while i <= 50:
    print(i)
    i += 2
